package com.kita.first;

public class Operator5 {
	public static void main(String[] args) {
		//비교연산자> boolean
		int n1 = 10;
		int n2 = 11;
		boolean result = (n1 <= n2);
		
		System.out.println(n1);
		
		result = (n1 != n2);
		System.out.println(result);
		
		System.out.println();
		System.out.println();
		result = n1 < n2;
		System.out.println(result);
		
	}

}
